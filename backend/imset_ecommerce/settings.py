# MongoDB Configuration
USE_MONGODB = os.getenv('USE_MONGODB', 'True') == 'True'

if USE_MONGODB:
    # MongoDB using djongo
    db_host = os.getenv('DB_HOST', 'localhost')
    db_port = int(os.getenv('DB_PORT', '27017'))
    db_name = os.getenv('DB_NAME', 'artgstore')  # MongoDB database name for ARTG Store e-commerce
    db_user = os.getenv('DB_USER', '')
    db_password = os.getenv('DB_PASSWORD', '')
    
    # Build MongoDB connection string
    if db_user and db_password:
        # Authenticated connection
        db_auth_source = os.getenv('DB_AUTH_SOURCE', 'admin')
        mongo_uri = f"mongodb://{db_user}:{db_password}@{db_host}:{db_port}/{db_name}?authSource={db_auth_source}"
    else:
        # Non-authenticated connection (local development)
        mongo_uri = f"mongodb://{db_host}:{db_port}/{db_name}"
    
    DATABASES = {
        'default': {
            'ENGINE': 'djongo',
            'NAME': db_name,
            'ENFORCE_SCHEMA': False,
            'CLIENT': {
                'host': mongo_uri,
            }
        }
    }
else:
    # SQLite for development (fallback)
    DATABASES = {
        'default': {
            'ENGINE': 'django.db.backends.sqlite3',
            'NAME': BASE_DIR / 'db.sqlite3',
        }
    }


